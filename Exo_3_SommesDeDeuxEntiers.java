/*Exo3: Ecrire un programme en java qui declare deux entiers, calcul leur somme et affiche le resultats*/

public class Exo_3_SommesDeDeuxEntiers {

    public static void main(String[] args) {
        int entier1 = 5;
        int entier2 = 10;

        int somme = entier1 + entier2;

        System.out.println("La somme de " + entier1 + " et " + entier2 + " est: " + somme);
    }
}
